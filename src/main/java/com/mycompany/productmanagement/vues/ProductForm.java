    private void btn_deleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_deleteActionPerformed
        if(txt_id.getText() == null){
            JOptionPane.showMessageDialog(null, "Aucun produit n'est sélectionné");
            
        } else {
            if(JOptionPane.showConfirmDialog(this, "Do you really want to delete this product", "Product Selected", 
                    JOptionPane.YES_NO_OPTION) == JOptionPane.YES_OPTION ){
                int id = Integer.parseInt(txt_id.getText());
                boolean deleted = ProductController.deleteProduct(id);
                if(deleted){
                    JOptionPane.showMessageDialog(null, "Product deleted");
                    showProductsInTable();
                } else {
                    JOptionPane.showMessageDialog(null, "Product deleted failed");
                }
            }
        }
    }//GEN-LAST:event_btn_deleteActionPerformed

