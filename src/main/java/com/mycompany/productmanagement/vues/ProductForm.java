    public boolean checkInputs(){
        if(txt_name.getText() == null 
                || txt_price.getText() == null 
                || txt_date.getDate() == null
                || imgPath == null 
                || imgPath.equals("")){
            return false;
        } else {
            try {
                Float.parseFloat(txt_price.getText());
                return true;
            } catch (NumberFormatException e){
                
            }
        }
        return false;
    }
    
    public Product createProductFromInputs(){
        if(checkInputs()){
            String name = txt_name.getText();
            String desString = txt_description.getText();
            String priceStr = txt_price.getText();
            Float price = Float.parseFloat(priceStr);
            Date date = txt_date.getDate();
            Product p;
            p = new Product(0, name, desString, price, date, imgPath);
            return p;
        }
        return null;
    }
    
    public void showProductsInTable()
    {
        DefaultTableModel model = (DefaultTableModel)tableProduits.getModel();
        if(list.size() > 0){
            for(int i = 0; i < list.size(); i++){
                //model.removeRow(i);
            }
        }
        list = ProductController.getAllProduct();
        Object[] row = new Object[5];
        for(int i = 0; i < list.size(); i++) {
            row[0] = list.get(i).getId();
            row[1] = list.get(i).getName();
            row[2] = list.get(i).getDescription();
            row[3] = list.get(i).getPrice();
            row[4] = list.get(i).getAddDate();
            
            model.addRow(row);
        }
    }
    // Show selected item in the the fields
    public void showItem(int index) {
        try {            
            txt_id.setText(Integer.toString(list.get(index).getId()));
            txt_name.setText(list.get(index).getName());
            txt_description.setText(list.get(index).getDescription());
            txt_price.setText(Float.toString(list.get(index).getPrice()));
            txt_date.setDate(list.get(index).getAddDate());
            ProductController.resizeImage(null, list.get(index).getImage(), lbl_image);
            
        } catch(Exception ex) {
            
        }
    }    

