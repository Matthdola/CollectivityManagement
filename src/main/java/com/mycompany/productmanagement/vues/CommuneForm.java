    private void btn_validerActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_validerActionPerformed
        Commune c = createCommuneFromInputs();
        if(c == null) {
            JOptionPane.showMessageDialog(null, "One or more field values not given");
        } else {
            boolean result = false;
            String operation = "";
            if(txt_id.getText() != null && Integer.parseInt(txt_id.getText()) > 0 ){
                c.setId(Integer.parseInt(txt_id.getText()));
                result = CommuneController.updateCommune(c);
                operation = "Modification";
            } else {
                result = CommuneController.insertCommune(c);
                operation = "Insertion";
            }
            
            if(result){
                PrincipalForm.communes.updateTable();
                setVisible(false);
                JOptionPane.showMessageDialog(null, operation + " effectué avec succès", operation +  "de commune", JOptionPane.INFORMATION_MESSAGE);
            } else {
                JOptionPane.showMessageDialog(null, "Erreur lors de la " + operation + " de la commune.");
            }           
        }
    }//GEN-LAST:event_btn_validerActionPerformed

