    public boolean checkInputs(){
        if(txt_nom.getText() == null 
                || txt_pop.getText() == null 
                || txt_sup.getText() == null){
            return false;
        } else {
            try {
                Float.parseFloat(txt_sup.getText());
                Integer.parseInt(txt_pop.getText());
                return true;
            } catch (NumberFormatException e){
                
            }
        }
        return false;
    }
    
    public Commune createCommuneFromInputs(){
        if(checkInputs()){
            String name = txt_nom.getText();
            String popString = txt_pop.getText();
            String supStr = txt_sup.getText();
            Float sup = Float.parseFloat(supStr);
            int pop = Integer.parseInt(popString);
            Commune commune;
            
            commune = new Commune(name, sup, pop);
            return commune;
        }
        return null;
    }
   
