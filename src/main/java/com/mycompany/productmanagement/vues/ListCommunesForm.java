    private void btn_supprimerActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_supprimerActionPerformed
        if(selectedCommune == null){
            JOptionPane.showMessageDialog(null, "Aucune commune n'est sélectionnée");
            
        } else {
            if(JOptionPane.showConfirmDialog(this, "Voulez-vous supprimer la commune sélectionnée", "Suppretion de commune", 
                    JOptionPane.YES_NO_OPTION) == JOptionPane.YES_OPTION ){
                boolean deleted = CommuneController.deleteCommune(selectedCommune.getId());
                if(deleted){
                    JOptionPane.showMessageDialog(null, "La commmune a été supprimé avec succès");
                    showCommunesInTable();
                } else {
                    JOptionPane.showMessageDialog(null, "La suppression de la commune a échoué");
                }
            }
        }
    }//GEN-LAST:event_btn_supprimerActionPerformed

