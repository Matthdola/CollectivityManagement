    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        JFileChooser file = new JFileChooser();
        file.setCurrentDirectory(new File(System.getProperty("user.home")));
        
        FileNameExtensionFilter filter = new FileNameExtensionFilter("*.images", "jpg", "png", "jpeg");
        file.addChoosableFileFilter(filter);
        int result = file.showSaveDialog(null);
        if(result == JFileChooser.APPROVE_OPTION){
            File selectedFile = file.getSelectedFile();
            String path = selectedFile.getAbsolutePath();
            lbl_image.setIcon(ProductController.resizeImage(path, null, lbl_image)); 
            imgPath = path;
        } else {
            JOptionPane.showMessageDialog(null, "Aucune image sélectionné");
        }
    }//GEN-LAST:event_jButton2ActionPerformed

    private void btn_insertActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_insertActionPerformed
        Product p = createProductFromInputs();
        if(p == null) {
            JOptionPane.showMessageDialog(null, "One or more field values not given");
        } else {
            boolean result = ProductController.insertProduct(p, imgPath);
            if(result){
                JOptionPane.showMessageDialog(null, "Product Inserted with success");
            } else {
                JOptionPane.showMessageDialog(null, "Error while inserting product ...");
            }           
        }
    }//GEN-LAST:event_btn_insertActionPerformed

