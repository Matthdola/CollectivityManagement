    private void showCommunesInTable()
    {        
        DefaultTableModel model = (DefaultTableModel)tableCommunes.getModel();
        int rows = model.getRowCount();
        
        if(list.size() > 0 && rows > 0){
            int i = 0;
            while(i < rows){
                model.removeRow(0);
                i++;
            }
        }
        list = CommuneController.getAllCommune();
        Object[] row = new Object[5];
        for(int i = 0; i < list.size(); i++) {
            row[0] = list.get(i).getId();
            row[1] = list.get(i).getNom();
            row[2] = list.get(i).getPopulation();
            row[3] = list.get(i).getSuperficie();
            
            model.addRow(row);
        }
    }
    
    public void updateTable(){
        showCommunesInTable();
    }

